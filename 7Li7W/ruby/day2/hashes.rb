# hashes.rb

puts "numbers = {1 => 'one', 2 => 'two'}"
puts numbers = {1 => 'one', 2 => 'two'}
puts "\r\n\r\n"

puts "numbers[1]"
puts numbers[1]
puts "\r\n\r\n"

puts "numbers[2]"
puts numbers[2]
puts "\r\n\r\n"

puts "stuff = {:array => [1, 2, 3], :string => 'Hi, mom!'}"
puts stuff = {:array => [1, 2, 3], :string => 'Hi, mom!'}
puts "\r\n\r\n"

puts "stuff[:string]"
puts stuff[:string]
puts "\r\n\r\n"

puts "stuff[:array]"
puts stuff[:array]
puts "\r\n\r\n"

